package com.pojo;


public class Buser {

  private long id;
  private String bpwd;
  private String bemail;

  public long getId() {
    return id;
  }

  public void setId(long id) {
    this.id = id;
  }


  public String getBpwd() {
    return bpwd;
  }

  public void setBpwd(String bpwd) {
    this.bpwd = bpwd;
  }


  public String getBemail() {
    return bemail;
  }

  public void setBemail(String bemail) {
    this.bemail = bemail;
  }

}
